# Boolean operations
# ******************

# NOT | TRUE?
# From the docs: The operator "not" yields "True" if its argument is false, "False" otherwise.
# the following values are interpreted as false: "False", "None", numeric zero of all types, and empty strings and containers
print("NOT | TRUE?")
print("not False:", not False) # True
print("not True:", not True) # False
print("not 0:", not 0) # True
print("not 1:", not 1) # False
print("not {}:", not {}) # True
print("not {0}:", not {0}) # False
print("***************************************\n")

# OR | TRUE?
# From the docs: The expression "x or y" first evaluates *x*; if *x* is true, its value is returned; otherwise, *y* is evaluated and the resulting value is returned.
print("OR | TRUE?")
print("True or False:", True or False) # True
print("True or True:", True or True) # True
print("False or True:", False or True) # True
print("False or False:", False or False) # False
print("1 or 0:", 1 or 0) # 1
print("1 or 1:", 1 or 1) # 1
print("0 or 1:", 0 or 1) # 1
print("0 or 0:", 0 or 0) # 0
print("***************************************\n")

# AND | TRUE?
# From the docs: The expression "x and y" first evaluates *x*; if *x* is false, its value is returned; otherwise, *y* is evaluated and the resulting value is returned.
print("AND | TRUE?")
print("True and False:", True and False) # False
print("True and True:", True and True) # True
print("False and True:", False and True) # False
print("False and False:", False and False) # False
print("***************************************\n")

# NOT OR | TRUE?
# If inside the bracket yields False, then not will yield True, otherwise if inside the bracket yields True, then not yeilds False
print("NOT OR | TRUE?")
print("not (True or False):", not (True or False)) # False  
print("not (True or True):", not (True or True)) # False
print("not (False or True):", not (False or True)) # False
print("not (False or False):", not (False or False)) # True
print("not (1 or 1):", not (1 or 1)) # False
print("not (1 or 0):", not (1 or 0)) # False
print("not (0 or 1):", not (0 or 1)) # False
print("not (0 or 0):", not (0 or 0)) # True
print("***************************************\n")

# NOT AND | TRUE?
# First term inside bracket is evaluated, if False, then it is returned to the not operator - otherwise second term is evaluated and returned to the not operator 
print("NOT AND | TRUE?")
print("not (True and False):", not (True and False)) # True
print("not (True and True):", not (True and True)) # False
print("not (False and True):", not (False and True)) # True
print("not (False and False):", not (False and False)) # True
print("not (1 and 0):", not (1 and 0)) # True
print("not (1 and 1):", not (1 and 1)) # False
print("not (0 and 1):", not (0 and 1)) # True
print("not (0 and 0):", not (0 and 0)) # True
print("***************************************\n")

# != | TRUE?
# comparison not equal to
print("!= | TRUE?")
print("1 != 0:", 1 != 0) # True
print("1 != 1:", 1 != 1) # False
print("0 != 1:", 0 != 1) # True
print("0 != 0:", 0 != 0) # False
print("***************************************\n")

# == | TRUE?
# comparison equal to
print("== | TRUE?")
print("1 == 0:", 1 == 0) # False
print("1 == 1:", 1 == 1) # True
print("0 == 1:", 0 == 1) # False
print("0 == 0:", 0 == 0) # True
print("***************************************\n")
